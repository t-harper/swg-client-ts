/**
 * Build the standard NGE skill chain for a profession. The chain is always
 * 24 skills: novice + 02/03/04/05 + master, repeated for 4 phases. The
 * `_01` variant does NOT exist in this fork — validated against `skills.tab`.
 */
function buildSkillChain(internalName: string): string[] {
  const chain: string[] = [];
  for (let phase = 1; phase <= 4; phase++) {
    chain.push(`class_${internalName}_phase${phase}_novice`);
    for (let level = 2; level <= 5; level++) {
      chain.push(`class_${internalName}_phase${phase}_0${level}`);
    }
    chain.push(`class_${internalName}_phase${phase}_master`);
  }
  return chain;
}

