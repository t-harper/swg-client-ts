/**
 * Per-profession provisioning specs for LIVE combat tests.
 *
 * Each spec carries the data needed to grant a fully-skilled, fully-geared
 * character for one of the six NGE combat professions:
 *
 *   - `skillChain`: every `class_<prof>_phase{N}_{novice,02..05,master}` skill
 *     in dependency order. `grantSkill` server-side enforces prereqs, so
 *     order matters. Validated against
 *     `~/code/swg-main/dsrc/sku.0/sys.shared/compiled/game/datatables/skill/skills.tab` —
 *     note: NO `_01` variant exists (skips from `_novice` to `_02`).
 *
 *   - `expertise`: every `expertise_<prefix>_*` leaf from
 *     `~/code/swg-main/dsrc/sku.0/sys.shared/compiled/game/datatables/expertise/expertise.tab`.
 *     Sent as ONE `ExpertiseRequestMessage` (god-mode bypasses point limits per
 *     `CreatureObject.cpp:14532`). Order doesn't matter for the wire message.
 *
 *   - `upgradeCommands`: tier-2/3 commands that `recomputeCommandSeries`
 *     doesn't reliably grant after admin XP grants. Granted directly via
 *     `skill grantCommand <cmd> <oid>` (which bypasses the upgrade machinery
 *     per `ConsoleCommandParserSkill.cpp:35`). Names match the highest-tier
 *     ability names from the bundled profession rotation.
 *
 *   - `weaponTemplate`: server template for the primary weapon, classified
 *     to match the bundled rotation's expected `weaponKind`. Spawned into
 *     inventory via `object createIn` and equipped via `transferItemMisc`.
 *
 *   - `npcSpawnTemplate`: default hostile NPC template for the LIVE test. Same
 *     standard humanoid the bot is expected to be able to handle as a level
 *     90 with full equipment. Override via `LIVE_COMBAT_SPAWN_TEMPLATE`.
 *
 *   - `skillTemplate` + `workingSkill` + `legacyProfession`: passed to
 *     `client.fullLifecycle({...})` at character-create time.
 *
 * Composite armor (the 9 chest/leggings/boots/etc. pieces) is shared across
 * every profession — listed separately in `COMPOSITE_ARMOR_TEMPLATES`.
 */

import type { ProfessionId } from '../../src/index.js';

export interface ProfessionProvisioningSpec {
  /** Character-creation NGE skillTemplate (e.g. `'bounty_hunter_1a'`). */
  skillTemplate: string;
  /** Character-creation working skill (e.g. `'class_bountyhunter_phase1_novice'`). */
  workingSkill: string;
  /** Character-creation legacy-profession stub (one of 7 PlayerCreationManager strings). */
  legacyProfession: string;
  /** Skill chain in dependency order. Granted sequentially via `skill grantSkill`. */
  skillChain: readonly string[];
  /** All expertise leaves for this profession. Sent as ONE ExpertiseRequestMessage. */
  expertise: readonly string[];
  /** Tier-2/3 commands granted directly to bypass the broken upgrade pipeline. */
  upgradeCommands: readonly string[];
  /** Weapon template (IFF path). Spawned + equipped at provisioning end. */
  weaponTemplate: string;
  /**
   * Armor pieces to spawn + equip. Empty for Jedi — composite-class armor
   * blocks saber wield (server-side wield permission check). Jedi rely on
   * `centerOfBeing` + `saberBlock` + saber-reflect expertise for defense.
   */
  armorPieces: readonly string[];
  /** Default hostile NPC template for the LIVE test. */
  npcSpawnTemplate: string;
}

